package org.fao.geonet.kernel.security;

import org.springframework.security.core.AuthenticationException;
import org.springframework.security.web.authentication.SimpleUrlAuthenticationFailureHandler;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

public class GeoNetworkAuthenticationFailureHandler
    extends SimpleUrlAuthenticationFailureHandler {

    public GeoNetworkAuthenticationFailureHandler() {
        super();
    }

    public GeoNetworkAuthenticationFailureHandler(String defaultFailureUrl) {
        super(defaultFailureUrl);
    }

    @Override
    public void onAuthenticationFailure(HttpServletRequest request, HttpServletResponse response, AuthenticationException exception) throws IOException, ServletException {

        String errorMessage = "auth.message.badCredentials";

        if (exception.getMessage()
            .equalsIgnoreCase("IP is blocked")) {
            errorMessage = "auth.message.ip.blocked";
        }

        request.getSession()
            .setAttribute("failureMsg", errorMessage);

        super.onAuthenticationFailure(request, response, exception);

    }
}
